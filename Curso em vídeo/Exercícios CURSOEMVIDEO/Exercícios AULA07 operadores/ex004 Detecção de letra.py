a=input("Digite algo: ")
print('A classe é: ')
print(type(a))
print('É numérico? ', (a.isnumeric()))
print('É alfanumérico? ', (a.isalnum()))
print('É decimal? ', (a.isdecimal()))
print('É todo maiúsculo?', (a.isupper()))
print('Possui espaços? ', a.isspace())
print('É minusculo? ', a.islower())



